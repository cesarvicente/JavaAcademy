package fundamentos;

public class AreaCircunferencia {
	
	public static void main(String[] args) {
		double raio = 3.4;
		final double PI = 3.14159;
		
		double area = PI * raio * raio;
		System.out.println("Raio Calculado= "+raio+"\n햞ea = "+area+"m\n");
		
		raio = 10;
		area = PI * raio * raio;
		System.out.println("Raio Calculado= "+raio+"\n햞ea = "+area+"m\n");
	} 

}
